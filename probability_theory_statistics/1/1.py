from math import factorial


# а) 1. найти кол-во сочетаний 4 из 13 (для треф)
# 2. найти кол-во всех сочетаний 4 из 52
# 3. поделить первое на второе

# б) 1. найти сумму произведений сочетаний 1 из 4 тузов, 2 из 4, ...
# 2. найти кол-во всех сочетаний без тузов  4 из 52
# 3. поделить первое на второе


def combin(n, m):
    return factorial(n) // (factorial(m) * factorial(n - m))


result = combin(13, 4) / combin(52, 4)
print(f'a) Вероятность того, что все карты треф = {result}')

