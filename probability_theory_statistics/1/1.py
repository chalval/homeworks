m2 = combin(4, 3) * combin(48, 1) + combin(4, 4) * combin(48, 0)
result2 = (m1 + m2) / combin(52, 4)
print(f'б) Вероятность того, что среди 4-х карт окажется хотя бы один туз = {result2}')
result3 = 1 - combin(48, 4) / combin(52, 4)
